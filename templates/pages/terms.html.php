    <h3 style="color: #ffff33; margin-top: 15px;">Пользователь обязуется:</h3>
    <div class="document-block">
        <ul>
            <li>Соблюдать условия настоящего соглашения и <a href="/rules" style="color: #ffff33;">правила Проекта</a></li>
            <li>Предоставлять достоверные данные при регистрации</li>
            <li>Не передавать доступ к своему аккаунту третьим лицам</li>
            <li>Не использовать читы, боты и другое запрещенное программное обеспечение</li>
            <li>Не нарушать игровой баланс и техническую стабильность сервера</li>
            <li>Соблюдать правила общения и не нарушать права других пользователей</li>
            <li>Регулярно знакомиться с обновлениями соглашения</li>
        </ul>
