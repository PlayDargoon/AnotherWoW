    <h2 class="section-title">2. Предмет соглашения</h2>
    <div class="document-block">
        Администрация предоставляет Пользователю следующие бесплатные услуги:
        <ul>
            <li>Доступ к игровому серверу Azeroth</li>
            <li>Регистрацию и использование личного кабинета на сайте</li>
            <li>Возможность создания и управления игровыми персонажами</li>
            <li>Участие в системе голосования за сервер</li>
            <li>Получение наград за голосование</li>
            <li>Мониторинг статистики сервера и персонажей</li>
            <li>Техническую поддержку в рамках возможностей Проекта</li>
        </ul>
        <p class="info" style="margin-top:10px;">Помимо перечисленных бесплатных услуг, Проект может предоставлять платные услуги посредством приобретения внутренних расчётных единиц — «Бонусов». Условия платных услуг, курс, порядок оплаты и поставки цифрового товара изложены в разделах 12–14 настоящего соглашения.</p>
