    <div class="document-section">
        <h2 class="section-title">Раздел I: Внутриигровое общение</h2>
    
    <h3 style="color: #ffff33; margin-top: 15px;"><span style="color: #ffffff; font-size: 16px; font-weight: bold;">1.</span> Глобальные каналы и общественный чат</h3>
    <div class="document-block">
        К понятию <strong style="color: #ffff33; font-size: 14px;">ГЛОБАЛЬНЫЙ КАНАЛ</strong> относятся каналы, связывающие игроков по внутриигровому территориальному признаку: «Общий», «Торговля», «Поиск спутников», «Оборона».<br><br>
        К понятию <strong style="color: #ffff33; font-size: 14px;">ОБЩЕСТВЕННЫЙ ЧАТ</strong> относятся каналы локального общения: «/сказать», «/крик», «/эмоция», «/группа», «/рейд», «/полебоя».
        
        <ul style="margin-top: 10px;">
            <li><strong style="color: #ffffff; font-size: 14px;">1.1</strong> В глобальных каналах и общественном чате запрещены оскорбления и дискриминация по любым признакам, личные нападки, насмешки и другие формы деструктивного поведения.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.2</strong> В глобальных каналах запрещено использование нецензурной лексики, непристойных или ненормативных выражений в любых формах.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.3</strong> В глобальных каналах запрещен <strong style="color: #ffff33;">КАПС</strong> (преимущественно заглавные буквы в сообщении).</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.4</strong> В глобальных каналах запрещен <strong style="color: #ffff33;">ФЛУД</strong> - повторение одинаковых сообщений или публикация бессмысленных сообщений для захламления чата.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.5</strong> В канале «Поиск спутников» ограничение на рекламные сообщения: 1 сообщение в 5 минут от игрока.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.6</strong> Запрещена публикация сообщений об обмене игровой валюты между персонажами.</li>
        </ul>
        
        <p style="margin-top: 15px;"><span class="gold">Наказание:</span> Ограничение чата от 30 до 500 минут в зависимости от серьезности нарушения.</p>
    </div>

    <h3 style="color: #ffff33; margin-top: 15px;"><span style="color: #ffffff; font-size: 16px; font-weight: bold;">2.</span> Реклама и распространение контента</h3>
    <div class="document-block">
        <ul>
            <li><strong style="color: #ffffff; font-size: 14px;">2.1</strong> Запрещена любая реклама предприятий, сайтов и организаций, не связанных с проектом Azeroth.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.2</strong> Запрещены сообщения, пропагандирующие насилие, разжигающие межнациональную рознь, содержащие политический или религиозный контекст.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.3</strong> Запрещено распространение <strong style="color: #ffff33;">ПОРНОГРАФИЧЕСКОГО КОНТЕНТА</strong>.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.4</strong> Запрещено распространение контента, очерняющего проект Azeroth и его администрацию.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.5</strong> Запрещены угрозы, <strong style="color: #ffff33;">ШАНТАЖ</strong>, вымогательство и ссылки на мошеннические ресурсы.</li>
        </ul>
        
        <p style="margin-top: 15px;"><span class="gold">Наказание:</span> Ограничение чата от 24 часов до перманентной блокировки аккаунта.</p>
    </div>

    <h3 style="color: #ffff33; margin-top: 15px;"><span style="color: #ffffff; font-size: 16px; font-weight: bold;">3.</span> Имена персонажей и питомцев</h3>
    <div class="document-block">
        <ul>
            <li><strong style="color: #ffffff; font-size: 14px;">3.1</strong> Запрещены имена, содержащие оскорбительный, порнографический, религиозный, политический или непристойный контекст.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">3.2</strong> Запрещено использование имен, имеющих отношение к администрации проекта Azeroth.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">3.3</strong> Запрещено использование специальных символов в именах.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">3.4</strong> Имена <strong style="color: #ffff33;">БЕЗДЕЙСТВУЮЩИХ ПЕРСОНАЖЕЙ</strong> могут быть аннулированы: до 60 уровня - 60 дней, от 60 до 80 - 180 дней бездействия.</li>
        </ul>
        
        <p style="margin-top: 15px;"><span class="gold">Наказание:</span> Принудительная смена имени с блокировкой аккаунта от 1 до 3 дней.</p>
    </div>

    <h2 class="section-title">Раздел II: Администрация проекта</h2>
    
    <h3 style="color: #ffff33; margin-top: 15px;"><span style="color: #ffffff; font-size: 16px; font-weight: bold;">1.</span> Общение с администрацией</h3>
    <div class="document-block">
        <ul>
            <li><strong style="color: #ffffff; font-size: 14px;">1.1</strong> Запрещено проявлять <strong style="color: #ffff33;">НЕУВАЖЕНИЕ</strong> к администрации проекта в любой форме.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.2</strong> Запрещено утаивать и фальсифицировать информацию при общении с администрацией.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.3</strong> Запрещены <strong style="color: #ffff33;">УГРОЗЫ</strong> в адрес администрации проекта.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.4</strong> Запрещено оспаривать решения администрации в игре.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.5</strong> Запрещено игнорирование требований администрации.</li>
        </ul>
        
        <p style="margin-top: 15px;"><span class="gold">Наказание:</span> Ограничение чата от 4 часов до блокировки аккаунта на 14 дней.</p>
    </div>

    <h3 style="color: #ffff33; margin-top: 15px;"><span style="color: #ffffff; font-size: 16px; font-weight: bold;">2.</span> Права администрации</h3>
    <div class="document-block">
        <ul>
            <li><strong style="color: #ffffff; font-size: 14px;">2.1</strong> Администрация следит за соблюдением правил проекта.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.2</strong> Администрация оказывает помощь игрокам по внутриигровым вопросам.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.3</strong> Администрация может отказать в помощи любому игроку без объяснения причин.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.4</strong> Администрация может поощрять игроков, способствующих развитию проекта.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.5</strong> Администрация может выносить наказания за действия, не описанные в правилах.</li>
        </ul>
    </div>

    <h2 class="section-title">Раздел III: Жульничество</h2>
    
    <h3 style="color: #ffff33; margin-top: 15px;"><span style="color: #ffffff; font-size: 16px; font-weight: bold;">1.</span> Баги и их использование</h3>
    <div class="document-block">
        Под <strong style="color: #ffff33; font-size: 14px;">БАГОМ</strong> подразумевается любая неполадка в механике игры, позволяющая получить преимущества в обход игровой механики.
        
        <ul style="margin-top: 10px;">
            <li><strong style="color: #ffffff; font-size: 14px;">1.1</strong> Запрещено намеренное использование багов с целью получения выгоды.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.2</strong> Запрещено использование багов с целью навредить серверу или другим игрокам.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.3</strong> <strong style="color: #ffff33;">УТАИВАНИЕ ИНФОРМАЦИИ</strong> о баге приравнивается к его использованию.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.4</strong> Распространение информации о багах запрещено.</li>
        </ul>
        
        <p style="margin-top: 15px;"><span class="gold">Наказание:</span> Блокировка аккаунта от 1 дня до перманентной с конфискацией имущества.</p>
    </div>

    <h3 style="color: #ffff33; margin-top: 15px;"><span style="color: #ffffff; font-size: 16px; font-weight: bold;">2.</span> Стороннее программное обеспечение</h3>
    <div class="document-block">
        <ul>
            <li><strong style="color: #ffffff; font-size: 14px;">2.1</strong> Запрещена автоматизация отправки сообщений.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.2</strong> Категорически запрещено использование ботов, кроме тех которых предоставляет игровой мир, читов, скриптов для получения преимуществ.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.3</strong> Запрещено утаивать информацию об использовании сторонних программ другими игроками.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.4</strong> Запрещено распространение информации о сторонних программах.</li>
        </ul>
        
        <p style="margin-top: 15px;"><span class="gold">Наказание:</span> Перманентная блокировка аккаунта без возможности восстановления.</p>
    </div>

    <h3 style="color: #ffff33; margin-top: 15px;"><span style="color: #ffffff; font-size: 16px; font-weight: bold;">3.</span> Мошенничество</h3>
    <div class="document-block">
        <ul>
            <li><strong style="color: #ffffff; font-size: 14px;">3.1</strong> Запрещены обманные действия при торговле (ложная информация в письмах, изменение условий сделки).</li>
            <li><strong style="color: #ffffff; font-size: 14px;">3.2</strong> Запрещено нарушение условий заключенных с игроками сделок.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">3.3</strong> Запрещен "ниндзя лутинг" - нарушение установленных правил распределения добычи в группах.</li>
        </ul>
        
        <p style="margin-top: 15px;"><span class="gold">Наказание:</span> Блокировка всех связанных аккаунтов от 7 до 14 дней с конфискацией имущества.</p>
    </div>

    <h2 class="section-title">Раздел IV: PvP активности</h2>
    
    <h3 style="color: #ffff33; margin-top: 15px;"><span style="color: #ffffff; font-size: 16px; font-weight: bold;">1.</span> Арена</h3>
    <div class="document-block">
        <ul>
            <li><strong style="color: #ffffff; font-size: 14px;">1.1</strong> Запрещены договорные матчи на арене в любом виде.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.2</strong> Запрещены матчи, где команды заведомо не стремятся к победе.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.3</strong> Игра с одного IP за разные команды считается договорным матчем.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.4</strong> Запрещено бездействие на арене.</li>
        </ul>
        
        <p style="margin-top: 15px;"><span class="gold">Наказание:</span> Дисквалификация с арены, возможна блокировка аккаунта от 1 дня до перманентной.</p>
    </div>

    <h3 style="color: #ffff33; margin-top: 15px;"><span style="color: #ffffff; font-size: 16px; font-weight: bold;">2.</span> Поля боя</h3>
    <div class="document-block">
        <ul>
            <li><strong style="color: #ffffff; font-size: 14px;">2.1</strong> Запрещено умышленно мешать своим союзникам на поле боя.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.2</strong> Запрещена пропаганда бездействия и призывы к проигрышу.</li>
        </ul>
        
        <p style="margin-top: 15px;"><span class="gold">Наказание:</span> Блокировка аккаунта от 1 до 7 дней или ограничение чата от 24 часов.</p>
    </div>

    <h2 class="section-title">Раздел V: Прочее</h2>
    
    <h3 style="color: #ffff33; margin-top: 15px;"><span style="color: #ffffff; font-size: 16px; font-weight: bold;">1.</span> Внутриигровая торговля</h3>
    <div class="document-block">
        <ul>
            <li><strong style="color: #ffffff; font-size: 14px;">1.1</strong> Запрещена торговля игровых ценностей за реальные деньги.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.2</strong> Запрещен обмен и продажа игровых персонажей.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.3</strong> Запрещена торговля аккаунтами.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.4</strong> Запрещены любые намеки на торговлю за реальные деньги.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">1.5</strong> Запрещены услуги "буста" за реальные деньги.</li>
        </ul>
        
        <p style="margin-top: 15px;"><span class="gold">Наказание:</span> Перманентная блокировка всех связанных аккаунтов без возможности восстановления.</p>
    </div>

    <h3 style="color: #ffff33; margin-top: 15px;"><span style="color: #ffffff; font-size: 16px; font-weight: bold;">2.</span> Пренебрежение наказанием</h3>
    <div class="document-block">
        <ul>
            <li><strong style="color: #ffffff; font-size: 14px;">2.1</strong> Запрещено повторное нарушение с другого аккаунта.</li>
            <li><strong style="color: #ffffff; font-size: 14px;">2.2</strong> Запрещено размещение сообщений другим игроком от имени нарушителя.</li>
        </ul>
        
        <p style="margin-top: 15px;"><span class="gold">Наказание:</span> Блокировка от 7 дней до перманентной блокировки всех связанных аккаунтов.</p>
    </div>

